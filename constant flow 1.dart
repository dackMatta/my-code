void main() {
  //not equal to
  const doesOneEqualTwo = (1 != 2);
  print(doesOneEqualTwo);
//equal to
  const doesTwoEqualFive = (2 == 2);
  print(doesTwoEqualFive);
//greater than or equal to
  const isGreater = (10 >= 10);
  print(isGreater);
//less than or equal to
  const isLess = (15 <= 15);
  print(isLess);
}
