void main() {
  //not equal to
  const doesOneEqualTwo = (2 != 4);
  print(doesOneEqualTwo);
//equal to
  const doesTwoEqualFive = (3 == 2);
  print(doesTwoEqualFive);
//greater than or equal to
  const isGreater = (1 >= 10);
  print(isGreater);
//less than or equal to
  const isLess = (17 <= 15);
  print(isLess);
}
